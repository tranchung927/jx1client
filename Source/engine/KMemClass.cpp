#include "KPlatform.h"
#include "KMemBase.h"
#include "KMemClass.h"
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    KMemClass
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝ๏ฟฝ๏ฟฝ์บฏ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
KMemClass::KMemClass()
{
    m_lpMemPtr = NULL;
    m_lpMemLen = 0;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ~KMemClass
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝ๏ฟฝ๏ฟฝ์บฏ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
KMemClass::~KMemClass()
{
    Free();
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Alloc
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    dwSize        ๏ฟฝฺด๏ฟฝ๏ฟฝ๏ฟฝะก
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    PVOID        ๏ฟฝฺด๏ฟฝ๏ฟฝึธ๏ฟฝ๏ฟฝ
//---------------------------------------------------------------------------
PVOID KMemClass::Alloc(DWORD dwSize)
{
    // ๏ฟฝัพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤพอฒ๏ฟฝาช๏ฟฝูท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    if (m_lpMemLen == dwSize)
        return m_lpMemPtr;

    // ๏ฟฝอท๏ฟฝ๏ฟฝัพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ
    if (m_lpMemPtr)
        Free();

    // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ
    m_lpMemPtr = g_MemAlloc(dwSize);
    if (m_lpMemPtr != NULL)
        m_lpMemLen = dwSize;
    return m_lpMemPtr;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Free
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝอท๏ฟฝ๏ฟฝฺด๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
void KMemClass::Free()
{
    if (m_lpMemPtr)
        g_MemFree(m_lpMemPtr);
    m_lpMemPtr = NULL;
    m_lpMemLen = 0;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Zero
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝฺด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
void KMemClass::Zero()
{
    if (m_lpMemPtr)
        g_MemZero(m_lpMemPtr, m_lpMemLen);
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Fill
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝฺด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    byFill    ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
void KMemClass::Fill(BYTE byFill)
{
    if (m_lpMemPtr)
        g_MemFill(m_lpMemPtr, m_lpMemLen, byFill);
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Fill
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝฺด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    byFill    ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
void KMemClass::Fill(WORD wFill)
{
    if (m_lpMemPtr)
        g_MemFill(m_lpMemPtr, m_lpMemLen, wFill);
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    Fill
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    ๏ฟฝฺด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    byFill    ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:    void
//---------------------------------------------------------------------------
void KMemClass::Fill(DWORD dwFill)
{
    if (m_lpMemPtr)
        g_MemFill(m_lpMemPtr, m_lpMemLen, dwFill);
}
