// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	AllocNewChunk
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยต๏ฟฝ๏ฟฝฺด๏ฟฝ้ฃฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮช0
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	TRUE	๏ฟฝษน๏ฟฝ
//			FALSE	สง๏ฟฝ๏ฟฝ
//---------------------------------------------------------------------------
int KMemStack::AllocNewChunk()
{
	if (m_nChunkTop >= MAX_CHUNK)
	{
//		g_MessageBox("KMemStack : Chunk over flow A");
		return FALSE;
	}

	m_pStack[m_nChunkTop] = (PBYTE)g_MemAlloc(m_nChunkSize);
	if (m_pStack[m_nChunkTop] == NULL)
	{
//		g_MessageBox("KMemStack : Chunk over flow B");
		return FALSE;
	}

	m_nChunkTop++;
	m_nStackTop = 0; //ีป๏ฟฝ๏ฟฝ
	m_nStackEnd = m_nChunkSize;
	return TRUE;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	FreeAllChunks
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝอท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะต๏ฟฝ๏ฟฝฺด๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
//---------------------------------------------------------------------------
void KMemStack::FreeAllChunks()
{
	for (int i = 0; i < m_nChunkTop; i++)
	{
		if (m_pStack[i]!=NULL)
		{
		   g_MemFree(m_pStack[i]);
		   m_pStack[i] = NULL;
		}
	}

	m_nStackTop = 0;
	m_nStackEnd = 0;
	m_nChunkTop = 0;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	GetChunkSize
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	ศก๏ฟฝรถ๏ฟฝีปChunk๏ฟฝฤด๏ฟฝะก
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	Chunk ๏ฟฝฤด๏ฟฝะก in bytes
//---------------------------------------------------------------------------
int KMemStack::GetChunkSize()
{
	return m_nChunkSize;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	GetStackSize
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	ศก๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ๏ฟฝีป๏ฟฝฤด๏ฟฝะก
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝฺด๏ฟฝ๏ฟฝีป๏ฟฝฤด๏ฟฝะก in bytes
//---------------------------------------------------------------------------
int KMemStack::GetStackSize()
{
	return (m_nStackTop + (m_nChunkTop - 1) * m_nChunkSize);
}
