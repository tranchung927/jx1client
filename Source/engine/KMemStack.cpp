//---------------------------------------------------------------------------
KMemStack::~KMemStack()
{
	FreeAllChunks();
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	Init
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	nChunkSize	Chunk๏ฟฝ฿ด๏ฟฝ๏ฟฝะก
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	void
//---------------------------------------------------------------------------
void KMemStack::Init(int nChunkSize)
{
	FreeAllChunks();
	m_nChunkSize = nChunkSize;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	Push
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	nSize	๏ฟฝฺด๏ฟฝ๏ฟฝะก
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	PVOID	๏ฟฝฺด๏ฟฝึธ๏ฟฝ๋ฃฌ= NULL ๏ฟฝ๏ฟฝสพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสง๏ฟฝ๏ฟฝ
//---------------------------------------------------------------------------
PVOID KMemStack::Push(int nSize)
{
	PBYTE pResult;

	if (m_nChunkSize < nSize)
		m_nChunkSize = nSize;

	if (m_nStackTop + nSize > m_nStackEnd)
	{
		if (!AllocNewChunk())
			return NULL;
	}

	pResult = m_pStack[m_nChunkTop - 1];
	pResult += m_nStackTop;
	m_nStackTop += nSize;
	return pResult;
}
