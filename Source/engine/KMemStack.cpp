KMemStack::KMemStack()
{
	g_MemZero(m_pStack, sizeof(m_pStack));
	m_nStackTop  = 0;
	m_nStackEnd  = 0;
	m_nChunkTop  = 0;
	m_nChunkSize = 65536;
}
//---------------------------------------------------------------------------
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	~KMemStack
// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
